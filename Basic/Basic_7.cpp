    cout<< a << " - " << b << " = " << a-b << endl; 
    cout<< c << " - " << d << " = " << c-d << endl;  
    cout<< a << " - " << d << " = " << a-d << endl;  
    cout<< a << " * " << b << " = " << a*b << endl;  
    cout<< c << " * " << d << " = " << c*d << endl;  
    cout<< a << " * " << d << " = " << a*d << endl;  
    cout<< a << " / " << b << " = " << a/b << endl;  
    cout<< c << " / " << d << " = " << a/b << endl;  
